// 1. getElementById()
// -. Penjelasan : Metode ini mengembalikan elemen yang memiliki        atribut id yang cocok dengan string yang diberikan.

// 2. getElementsByName()
// -. Metode ini mengembalikan NodeList dari semua elemen dengan atribut name yang cocok. Ini biasanya digunakan dengan elemen form seperti <input>.

// 3. getElementsByClassName()
// -. Metode ini mengembalikan HTMLCollection dari semua elemen yang memiliki kelas yang cocok

// 4. GetElementsByTagName()
// -. Metode ini mengembalikan HTMLCollection dari semua elemen dengan nama tag yang cocok

// 5. querySelector()
// -. Metode ini mengembalikan elemen pertama dalam dokumen yang cocok dengan selektor CSS yang diberikan.

// 6. querySelectorAll()
// Metode ini mengembalikan NodeList dari semua elemen dalam dokumen yang cocok dengan selektor CSS yang diberikan.